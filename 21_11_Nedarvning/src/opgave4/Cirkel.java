package opgave4;

public class Cirkel extends Figur {

    // ===========================================================
    // Fields
    // ===========================================================
    private double radius;

    // ===========================================================
    // Constructors
    // ===========================================================
    public Cirkel(double xPos, double yPos, double radius) {
        super(xPos, yPos);
        this.radius = radius;
    }
    // ===========================================================
    // Getter & Setter
    // ===========================================================

    // ===========================================================
    // Methods
    // ===========================================================
    @Override
    public double beregnAreal() {
        return 3.14 * (radius * radius);
    }
}
